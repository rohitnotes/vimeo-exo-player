
package com.vikas.videoplayer.vimeo;


public class Request {

    private Files files;
    private String lang;
    private Sentry sentry;
    private AbTests abTests;
    private Object referrer;
    private String cookieDomain;
    private Integer timestamp;
    private GcDebug gcDebug;
    private Integer expires;
    private String currency;
    private String session;
    private Cookie cookie;
    private Build build;
    private Urls urls;
    private String signature;
    private Flags flags;
    private String country;
    private FileCodecs fileCodecs;

    public Files getFiles() {
        return files;
    }

    public void setFiles(Files files) {
        this.files = files;
    }

    public String getLang() {
        return lang;
    }

    public void setLang(String lang) {
        this.lang = lang;
    }

    public Sentry getSentry() {
        return sentry;
    }

    public void setSentry(Sentry sentry) {
        this.sentry = sentry;
    }

    public AbTests getAbTests() {
        return abTests;
    }

    public void setAbTests(AbTests abTests) {
        this.abTests = abTests;
    }

    public Object getReferrer() {
        return referrer;
    }

    public void setReferrer(Object referrer) {
        this.referrer = referrer;
    }

    public String getCookieDomain() {
        return cookieDomain;
    }

    public void setCookieDomain(String cookieDomain) {
        this.cookieDomain = cookieDomain;
    }

    public Integer getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(Integer timestamp) {
        this.timestamp = timestamp;
    }

    public GcDebug getGcDebug() {
        return gcDebug;
    }

    public void setGcDebug(GcDebug gcDebug) {
        this.gcDebug = gcDebug;
    }

    public Integer getExpires() {
        return expires;
    }

    public void setExpires(Integer expires) {
        this.expires = expires;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getSession() {
        return session;
    }

    public void setSession(String session) {
        this.session = session;
    }

    public Cookie getCookie() {
        return cookie;
    }

    public void setCookie(Cookie cookie) {
        this.cookie = cookie;
    }

    public Build getBuild() {
        return build;
    }

    public void setBuild(Build build) {
        this.build = build;
    }

    public Urls getUrls() {
        return urls;
    }

    public void setUrls(Urls urls) {
        this.urls = urls;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }

    public Flags getFlags() {
        return flags;
    }

    public void setFlags(Flags flags) {
        this.flags = flags;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public FileCodecs getFileCodecs() {
        return fileCodecs;
    }

    public void setFileCodecs(FileCodecs fileCodecs) {
        this.fileCodecs = fileCodecs;
    }

}
