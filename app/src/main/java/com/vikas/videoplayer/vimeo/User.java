
package com.vikas.videoplayer.vimeo;


public class User {

    private Object teamOriginUserId;
    private Integer liked;
    private String accountType;
    private String vimeoApiClientToken;
    private Object vimeoApiInteractionTokens;
    private Object teamId;
    private Integer watchLater;
    private Integer owner;
    private Integer id;
    private Boolean mod;
    private Integer loggedIn;

    public Object getTeamOriginUserId() {
        return teamOriginUserId;
    }

    public void setTeamOriginUserId(Object teamOriginUserId) {
        this.teamOriginUserId = teamOriginUserId;
    }

    public Integer getLiked() {
        return liked;
    }

    public void setLiked(Integer liked) {
        this.liked = liked;
    }

    public String getAccountType() {
        return accountType;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public String getVimeoApiClientToken() {
        return vimeoApiClientToken;
    }

    public void setVimeoApiClientToken(String vimeoApiClientToken) {
        this.vimeoApiClientToken = vimeoApiClientToken;
    }

    public Object getVimeoApiInteractionTokens() {
        return vimeoApiInteractionTokens;
    }

    public void setVimeoApiInteractionTokens(Object vimeoApiInteractionTokens) {
        this.vimeoApiInteractionTokens = vimeoApiInteractionTokens;
    }

    public Object getTeamId() {
        return teamId;
    }

    public void setTeamId(Object teamId) {
        this.teamId = teamId;
    }

    public Integer getWatchLater() {
        return watchLater;
    }

    public void setWatchLater(Integer watchLater) {
        this.watchLater = watchLater;
    }

    public Integer getOwner() {
        return owner;
    }

    public void setOwner(Integer owner) {
        this.owner = owner;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Boolean getMod() {
        return mod;
    }

    public void setMod(Boolean mod) {
        this.mod = mod;
    }

    public Integer getLoggedIn() {
        return loggedIn;
    }

    public void setLoggedIn(Integer loggedIn) {
        this.loggedIn = loggedIn;
    }

}
