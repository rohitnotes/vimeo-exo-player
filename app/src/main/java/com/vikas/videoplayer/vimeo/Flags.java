
package com.vikas.videoplayer.vimeo;


public class Flags {

    private Integer dnt;
    private String preloadVideo;
    private Integer plays;
    private Integer partials;
    private Integer autohideControls;

    public Integer getDnt() {
        return dnt;
    }

    public void setDnt(Integer dnt) {
        this.dnt = dnt;
    }

    public String getPreloadVideo() {
        return preloadVideo;
    }

    public void setPreloadVideo(String preloadVideo) {
        this.preloadVideo = preloadVideo;
    }

    public Integer getPlays() {
        return plays;
    }

    public void setPlays(Integer plays) {
        this.plays = plays;
    }

    public Integer getPartials() {
        return partials;
    }

    public void setPartials(Integer partials) {
        this.partials = partials;
    }

    public Integer getAutohideControls() {
        return autohideControls;
    }

    public void setAutohideControls(Integer autohideControls) {
        this.autohideControls = autohideControls;
    }

}
